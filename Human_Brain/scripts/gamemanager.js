function onClickBrainParts(n)
{

  SetActive(p_parietalLobeFullBrainId,false);
  SetActive(p_parietalLobeFullBrain2Id,false);
  SetActive(p_temporalLobeFullBrainId,false);
  SetActive(p_temporalLobeFullBrain2Id,false);
  SetActive(p_occipitalLobFullBrainId,false);
  SetActive(p_occipitalLobFullBrain2Id,false);
  SetActive(p_frontalLobeFullBrainId,false);
  SetActive(p_frontalLobeFullBrain2Id,false);

  switch (n)
  {
    case 1:
    SetActive(p_parietalLobeFullBrainId,true);
    PlayVO2(parietal_DiscriptionId);
    clickCount++;
    SetOnClickListener(parietalLobeFullBrainId,'');
    break;

    case 2:
    SetActive(p_parietalLobeFullBrain2Id,true);
    PlayVO2(parietal_DiscriptionId);
    clickCount++;
    SetOnClickListener(parietalLobeFullBrain2Id,'');
    break;

    case 3:
    SetActive(p_temporalLobeFullBrainId,true);
    PlayVO2(temporal_DiscriptionId);
    clickCount++;
    SetOnClickListener(temporalLobeFullBrainId,'');
    break;

    case 4:
    SetActive(p_temporalLobeFullBrain2Id,true);
    PlayVO2(temporal_DiscriptionId);
    clickCount++;
    SetOnClickListener(temporalLobeFullBrain2Id,'');
    break;

    case 5:
    SetActive(p_occipitalLobFullBrainId,true);
    PlayVO2(occipital_discriptionId);
    clickCount++;
    SetOnClickListener(occipitalLobFullBrainId,'');
    break;

    case 6:
    SetActive(p_occipitalLobFullBrain2Id,true);
    PlayVO2(occipital_discriptionId);
    clickCount++;
    SetOnClickListener(occipitalLobFullBrain2Id,'');
    break;

    case 7:
    SetActive(p_frontalLobeFullBrainId,true);
    PlayVO2(frontal_Lobe_DiscriptionId);
    clickCount++;
    SetOnClickListener(frontalLobeFullBrainId,'');
    break;

    case 8:
    SetActive(p_frontalLobeFullBrain2Id,true);
    PlayVO2(frontal_Lobe_DiscriptionId);
    clickCount++;
    SetOnClickListener(frontalLobeFullBrain2Id,'');
    break;
  }
  utility_changeText(i_itemsLeftId,"There are "+(8-clickCount)+" left");
  counterCheck();
}

